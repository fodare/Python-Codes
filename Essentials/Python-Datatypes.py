# DataTypes are basically telling the computer the type of data format we are working with.
"""
Some of the built in Python Datatypes
Text Type:	str
Numeric Types:	int, float, complex
Sequence Types:	list, tuple, range
Mapping Type:	dict
Set Types:	set, frozenset
Boolean Type:	bool
Binary Types:	bytes, bytearray, memoryview
"""

# Example
x = 10  # This is an integer as we are dealing with whole numbers
print(type(x))
y = 'Hello World'
print(type(y))

z = ["Damilare", "Rita", "Candyrose"]
print(type(z))

a = ("Damiilare", "Awais", "Anita", "Yonus")
print(type(a))
